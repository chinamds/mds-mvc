@Entity
@Table(name="cm_contentlist", uniqueConstraints = @UniqueConstraint(columnNames={"organization_id", "content_name"}))
@Indexed
@XmlRootElement
public class ContentList extends DataEntity implements Serializable {
    /**
	 * 
	 */
	private static final long serialVersionUID = -6423568646915753229L;
	private String contentName;
    private String description;
    
    private List<ContentListItem> contentListItems = Lists.newArrayList();
    private Organization organization;	// organization
    
    @ManyToOne(optional=true, fetch = FetchType.LAZY)
	@JoinColumn(name="organization_id", nullable=true)
	@NotFound(action = NotFoundAction.IGNORE)
	@JsonIgnore
	public Organization getOrganization() {
		return organization;
	}

	public void setOrganization(Organization organization) {
		this.organization = organization;
	}
	
	@Transient
	@Field
	public String getOrganizationCode() {
		if (organization != null && !organization.isRoot())
			return organization.getCode();
		
		return "";
	}
 
    @OneToMany(cascade=CascadeType.ALL, fetch = FetchType.LAZY, orphanRemoval = true, mappedBy="contentList")
	@OrderBy(value="id") 
	@Fetch(FetchMode.SUBSELECT)
	@NotFound(action = NotFoundAction.IGNORE)
	
    public List<ContentListItem> getContentListItems() {
		return contentListItems;
	}

	public void setContentListItems(List<ContentListItem> contentListItems) {
		this.contentListItems = contentListItems;
	}

	@JsonProperty(value = "contentName")
    @Column(name="content_name", nullable=false, length=50)
    @Field
    public String getContentName(){
        return this.contentName;
    }
    
    public void setContentName(String contentName){
        this.contentName = contentName;
    }
    
    @JsonProperty(value = "description")
    @Column(name="description", nullable=false, length=256)
    @Field
    public String getDescription(){
        return this.description;
    }
    
    public void setDescription(String description){
        this.description = description;
    }
    
    @JsonIgnore
	@Transient
    public Long getThumbnailContentObjectId(){
		if (!this.contentListItems.isEmpty())
			if (!this.contentListItems.get(0).getContentListZones().isEmpty())
				return this.contentListItems.get(0).getContentListZones().get(0).getContentObject().getId();
		
		return 0L;
    }
    
    /**
    * {@inheritDoc}
    */
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        ContentList pojo = (ContentList)o;
        return (new EqualsBuilder()
             .append(contentName, pojo.contentName)
             .append(description, pojo.description)
             ).isEquals();
    }


    /**
    * {@inheritDoc}
    */
     public int hashCode() {
        return   new  HashCodeBuilder( 17 ,  37 )
             .append(contentName)
             .append(description)
             .toHashCode();
    }


    /**
    * {@inheritDoc}
    */
     public String toString() {
        StringBuffer sb = new StringBuffer(getClass().getSimpleName());
        sb.append(" [");
        sb.append("Id").append("='").append(getId()).append("', ");
        sb.append("contentName").append("='").append(getContentName()).append("', ");
        sb.append("description").append("='").append(getDescription()).append("', ");
        sb.append("]");
        
        return sb.toString();
    }

}